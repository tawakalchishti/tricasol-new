                        <h4>WHAT IS AN SSL CERTIFICATE AND HOW DOES IT WORK?</h4>

                        <p class="pb-25">One of the most important components of online business is creating a trusted
                            environment where potential customers feel confident in making purchases. SSL certificates
                            create a foundation of trust by establishing a secure connection. To ensure visitors their
                            connection is secure, browsers provide visual cues, such as a lock icon or a green bar.

                            SSL certificates have a key pair: a public and a private key. These keys work together to
                            establish an encrypted connection. The certificate also contains what is called the
                            “subject,” which is the identity of the certificate/website owner.

                            To get a certificate, you must create a Certificate Signing Request (CSR) on your server.
                            This process creates a private key and public key on your server. The CSR data file that you
                            send to the SSL Certificate issuer (called a Certificate Authority or CA) contains the
                            public key. The CA uses the CSR data file to create a data structure to match your private
                            key without compromising the key itself. The CA never sees the private key.

                            Once you receive the SSL certificate, you install it on your server. You also install an
                            intermediate certificate that establishes the credibility of your SSL Certificate by tying
                            it to your CA’s root certificate. The instructions for installing and testing your
                            certificate will be different depending on your server.

                            In the image below, you can see what is called the certificate chain. It connects your
                            server certificate to your CA’s (in this case DigiCert’s) root certificate through an
                            intermediate certificate.

