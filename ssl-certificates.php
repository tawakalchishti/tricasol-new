                        <h4>How Does the SSL Certificate Create a secure Connection?</h4>

                        <p class="pb-25">When a browser attempts to access a website that is secured by SSL, the browser
                            and the web server establish an SSL connection using a process called an “SSL Handshake”
                            (see diagram below). Note that the SSL Handshake is invisible to the user and happens
                            instantaneously.

                            Essentially, three keys are used to set up the SSL connection: the public, private, and
                            session keys. Anything encrypted with the public key can only be decrypted with the private
                            key, and vice versa.

                            Because encrypting and decrypting with private and public key takes a lot of processing
                            power, they are only used during the SSL Handshake to create a symmetric session key. After
                            the secure connection is made, the session key is used to encrypt all transmitted data.

